/*
concat
Joins two or more strings, and returns a new joined strings
*/

var str1 = "Hello ";
var str2 = "world!";
var res = str1.concat(str2); // Hello world!

var str1 = "Hello ";
var str2 = "world!";
var str3 = " Have a nice day!";
var res = str1.concat(str2, str3); // Hello world! Have a nice day!